const profilesById = new Map(roleProfiles.map((item) => [item.id, item]));
const profilesByAlias = new Map(
  roleProfiles.flatMap((item) =>
    [item.name, ...item.aliases].map((name) => [normalizeRoleName(name), item])
  )
);

export function getRoleProfile(roleIdOrName) {
  if (!roleIdOrName) return roleProfiles[0];
  return profilesById.get(roleIdOrName) || profilesByAlias.get(normalizeRoleName(roleIdOrName)) || roleProfiles[0];
}

function profile(id, name, aliases, family, jdCount, detail) {
  return {
    id,
    name,
    aliases,
    family,
    jdCount,
    tasks: detail.tasks,
    skills: detail.skills.map(([skill, level, explain, evidence]) => ({ skill, level, explain, evidence })),
    hardReqs: detail.hardReqs,
    coreAbilities: detail.coreAbilities.map(([label, desc]) => ({ label, desc })),
    packagable: detail.packagable.map(([opportunity, status, note]) => ({ opportunity, status, note })),
  };
}

function normalizeRoleName(name) {
  return String(name).replace(/\s+/g, "").toLowerCase();
}
